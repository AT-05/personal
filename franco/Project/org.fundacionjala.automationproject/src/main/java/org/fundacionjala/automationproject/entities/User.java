public class User {
  private String firstName;
  private String lastName;
  private String phone;
  private String email;
  private String address;
  private String city;
  private String state;
  private String zip;
  private String country;
  private String userName;
  private String password;

  /**
   * <p>This method sets user first name value.</p>
   *
   * @param firstName is the user first name value given.
   */
  public void setFirstName(String firstName) {
    this.firstName = firstName;
  }

  /**
   * <p>This method sets user last name value.</p>
   *
   * @param lastName is the user last name value given.
   */
  public void setLastName(String lastName) {
    this.lastName = lastName;
  }

  /**
   * <p>This method sets user phone  value.</p>
   *
   * @param phone is the user phone value given.
   */
  public void setPhone(String phone) {
    this.phone = phone;
  }

  /**
   * <p>This method sets user emailvalue.</p>
   *
   * @param email is the user email value given.
   */
  public void setEmail(String email) {
    this.email = email;
  }

  /**
   * <p>This method sets user address value.</p>
   *
   * @param address is the user address value given.
   */
  public void setAddress(String address) {
    this.address = address;
  }

  /**
   * <p>This method sets user city value.</p>
   *
   * @param city is the user city value given.
   */
  public void setCity(String city) {
    this.city = city;
  }

  /**
   * <p>This method sets user state value.</p>
   *
   * @param state is the user state value given.
   */
  public void setState(String state) {
    this.state = state;
  }

  /**
   * <p>This method sets user postal code value.</p>
   *
   * @param zip is the user postal code value given.
   */
  public void setZip(String zip) {
    this.zip = zip;
  }

  /**
   * <p>This method sets user country value.</p>
   *
   * @param country is the user country value given.
   */
  public void setCountry(String country) {
    this.country = country;
  }

  /**
   * <p>This method sets username value.</p>
   *
   * @param userName is the username value given.
   */
  public void setUserName(String userName) {
    this.userName = userName;
  }

  /**
   * <p>This method sets user password value.</p>
   *
   * @param password is the user password value given.
   */
  public void setPassword(String password) {
    this.password = password;
  }

  /**
   * <p>This method gets user first name.</p>
   *
   * @return user first name value.
   */
  public String getFirstName() {
    return firstName;
  }

  /**
   * <p>This method gets user last name.</p>
   *
   * @return user last name value.
   */
  public String getLastName() {
    return lastName;
  }

  /**
   * <p>This method gets user phone.</p>
   *
   * @return user phone value.
   */
  public String getPhone() {
    return phone;
  }

  /**
   * <p>This method gets user email.</p>
   *
   * @return user email value.
   */
  public String getEmail() {
    return email;
  }

  /**
   * <p>This method gets user address.</p>
   *
   * @return user address value.
   */
  public String getAddress() {
    return address;
  }

  /**
   * <p>This method gets user city.</p>
   *
   * @return user city value.
   */
  public String getCity() {
    return city;
  }

  /**
   * <p>This method gets user state.</p>
   *
   * @return user state value.
   */
  public String getState() {
    return state;
  }

  /**
   * <p>This method gets user postal code.</p>
   *
   * @return user postal code value.
   */
  public String getZip() {
    return zip;
  }

  /**
   * <p>This method gets user country.</p>
   *
   * @return user country value.
   */
  public String getCountry() {
    return country;
  }

  /**
   * <p>This method gets userName.</p>
   *
   * @return userName value.
   */
  public String getUserName() {
    return userName;
  }

  /**
   * <p>This method gets user password.</p>
   *
   * @return user password value.
   */
  public String getPassword() {
    return password;
  }
}
